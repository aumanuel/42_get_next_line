#include "libft.h"

void	ft_puttab(char **tab)
{
	int		i;
	int		j;

	if (!tab)
		return ;
	i = -1;
	j = 0;
	while (tab[++i])
	{
		while (tab[i][j])
			ft_putchar(tab[i][j++]);
		ft_putchar('\n');
		j = 0;
	}
}
