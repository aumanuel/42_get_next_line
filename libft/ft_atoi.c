int		ft_atoi(const char *str)
{
	long		i;
	long		j;
	long		k;

	i = 0;
	j = 1;
	k = 0;
	while (str[i] == ' ' || str[i] == '\f' || str[i] == '\r' ||
			str[i] == '\n' || str[i] == '\t' || str[i] == '\v')
		i++;
	if (str[i] == '-')
	{
		j = -j;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
		k = k * 10 + (str[i++] - '0');
	return (k * j);
}
