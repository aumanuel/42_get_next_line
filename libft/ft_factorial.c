int		ft_factorial(int n)
{
	int		i;

	i = 1;
	if (n < 0 || n > 12)
		return (0);
	if (n == 0 || n == 1)
		return (1);
	else
		while (n-- > 1)
			i = i * (n + 1);
	return (i);
}
